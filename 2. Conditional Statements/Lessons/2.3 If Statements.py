# an if statement will only execute when comparison operators evaluate to True
#if statements can be used with string and numeric comparison operators



#If statement with numeric comparison
age = 20

if age <= 13:
    print("Sorry! You cannot watch this movie")
elif age <= 17:
    print("You can watch this movie with parent permission")
else:
    print("You can watch the movie")



#If statement with string comparison
mood = "happy"

if mood == "happy":
    print("clap your hands")
else:
    print("Lets go get ice cream")


